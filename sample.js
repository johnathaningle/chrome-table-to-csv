window.exportData = null;

function onClickHandler(info, tab) {
    if(!exportData){
        alert('No table data found!');
    }else{
        var rows = [];
        rows.push(exportData.labels);
        rows = rows.concat(exportData.values);
        if(info.menuItemId == 'export_cvs'){
            exportToCsv('data_' + Date.now(), rows);
        }else if(info.menuItemId == 'export_tsv'){
            exportTSV('data_' + Date.now(), rows);
        }

    }
};

chrome.runtime.onInstalled.addListener(function() {
  chrome.contextMenus.create({"title": "Save Data to CSV", "id": "export_cvs"}, function() {

  });
  chrome.contextMenus.create({"title": "Save Data to txt", "id": "export_tsv"}, function() {

  });
  chrome.contextMenus.onClicked.addListener(onClickHandler);
});


chrome.runtime.onMessage.addListener(function(request, sender, sendResponse) {
    exportData = request;
    sendResponse({})
});
